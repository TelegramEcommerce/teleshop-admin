export function normalizeText(str) {
  if (!str) return str;

  for (const c of str) {
    const cp = c.codePointAt(0);
    if (cp >= 0x1000 && cp <= 0x109F) return str;
  }

  const hasStyled = [...str].some(c => map.has(c.codePointAt(0)));
  if (!hasStyled) return str;

  return [...str].map(c => {
    const cp = c.codePointAt(0);
    const mapped = map.get(cp);
    return mapped !== undefined ? String.fromCodePoint(mapped) : c;
  }).join('');
}
